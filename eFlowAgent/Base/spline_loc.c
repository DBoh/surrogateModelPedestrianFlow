#include <ped.h>


REAL cspline_loc(int flag, REAL c[4][4], REAL x, REAL y)
{
  extern INFOS msg;
  int i, j, l;
  REAL mat[4][4], pol[4]={0.,0.,0.,0.}, koef[4]={0.,0.,0.,0.}, fac;
  char bla[200];

  REAL out = 0., p1, p2, p3, p4;
  REAL a31, a21, a11, a01;

  if(msg.Warning && (x<1. || x>2. || y<1. || y>2.)){
      WriteLog("MSG from <cspline_loc>:\n","a");
      sprintf(bla,"W: (xl,yl) should be in [1,2]x[1,2] -> (%f,%f) \n",x,y);
      WriteLog(bla,"a");
  }

  switch (flag){
  case 1:

    fac = 1./15.;
    mat[0][0] = -5; mat[0][1] = 15; mat[0][2] =-15; mat[0][3] =  5;
    mat[1][0] = 27; mat[1][1] =-72; mat[1][2] = 63; mat[1][3] =-18;
    mat[2][0] =-46; mat[2][1] = 96; mat[2][2] =-69; mat[2][3] = 19;
    mat[3][0] = 24; mat[3][1] =-24; mat[3][2] = 21; mat[3][3] = -6;

    for(l=0 ; l<4 ; l++){
      for(i=0 ; i<4 ; i++){
  koef[3-i]=0.;
  for(j=0 ; j<4 ; j++)
    koef[3-i] += mat[i][j]*c[j][l]*fac;
      }
      pol[l]=0.;
      for(i=0 ; i<4 ; i++)
  pol[l] += koef[i]*pow(x,1.*i);
    }

    for(i=0 ; i<4 ; i++){
      koef[3-i]=0.;
      for(j=0 ; j<4 ; j++)
  koef[3-i] += mat[i][j]*pol[j]*fac;
    }

    out=0.;
    for(i=0 ; i<4 ; i++)
      out += koef[i]*pow(y,1.*i);

    break;
  case 2: // same as case 1:

    a31 = - 1.*c[0][0]/3.  +  1.*c[1][0]    -  1.*c[2][0]    +  1.*c[3][0]/3.;
    a21 =   9.*c[0][0]/5.  - 24.*c[1][0]/5. + 21.*c[2][0]/5. -  6.*c[3][0]/5.;
    a11 = -46.*c[0][0]/15. + 32.*c[1][0]/5. - 23.*c[2][0]/5. + 19.*c[3][0]/15.;
    a01 =   8.*c[0][0]/5.  -  8.*c[1][0]/5. +  7.*c[2][0]/5. -  2.*c[3][0]/5.;

    p1 = a31*pow(x,3.) + a21*pow(x,2.) + a11*x + a01;

    a31 = - 1.*c[0][1]/3.  +  1.*c[1][1]    -  1.*c[2][1]    +  1.*c[3][1]/3.;
    a21 =   9.*c[0][1]/5.  - 24.*c[1][1]/5. + 21.*c[2][1]/5. -  6.*c[3][1]/5.;
    a11 = -46.*c[0][1]/15. + 32.*c[1][1]/5. - 23.*c[2][1]/5. + 19.*c[3][1]/15.;
    a01 =   8.*c[0][1]/5.  -  8.*c[1][1]/5. +  7.*c[2][1]/5. -  2.*c[3][1]/5.;

    p2 = a31*pow(x,3.) + a21*pow(x,2.) + a11*x + a01;

    a31 = - 1.*c[0][2]/3.  +  1.*c[1][2]    -  1.*c[2][2]    +  1.*c[3][2]/3.;
    a21 =   9.*c[0][2]/5.  - 24.*c[1][2]/5. + 21.*c[2][2]/5. -  6.*c[3][2]/5.;
    a11 = -46.*c[0][2]/15. + 32.*c[1][2]/5. - 23.*c[2][2]/5. + 19.*c[3][2]/15.;
    a01 =   8.*c[0][2]/5.  -  8.*c[1][2]/5. +  7.*c[2][2]/5. -  2.*c[3][2]/5.;

    p3 = a31*pow(x,3.) + a21*pow(x,2.) + a11*x + a01;

    a31 = - 1.*c[0][3]/3.  +  1.*c[1][3]    -  1.*c[2][3]    +  1.*c[3][3]/3.;
    a21 =   9.*c[0][3]/5.  - 24.*c[1][3]/5. + 21.*c[2][3]/5. -  6.*c[3][3]/5.;
    a11 = -46.*c[0][3]/15. + 32.*c[1][3]/5. - 23.*c[2][3]/5. + 19.*c[3][3]/15.;
    a01 =   8.*c[0][3]/5.  -  8.*c[1][3]/5. +  7.*c[2][3]/5. -  2.*c[3][3]/5.;

    p4 = a31*pow(x,3.) + a21*pow(x,2.) + a11*x + a01;

    a31 = - 1.*p1/3.  +  1.*p2    -  1.*p3    +  1.*p4/3.;
    a21 =   9.*p1/5.  - 24.*p2/5. + 21.*p3/5. -  6.*p4/5.;
    a11 = -46.*p1/15. + 32.*p2/5. - 23.*p3/5. + 19.*p4/15.;
    a01 =   8.*p1/5.  -  8.*p2/5. +  7.*p3/5. -  2.*p4/5.;

    out = a31*pow(y,3.) + a21*pow(y,2.) + a11*y + a01;

    break;
  }

  return out;

}
